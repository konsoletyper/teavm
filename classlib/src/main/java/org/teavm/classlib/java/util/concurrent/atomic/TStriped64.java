package org.teavm.classlib.java.util.concurrent.atomic;

/**
 * Base class for LongAdder, LongAccumulator, DoubleAdder, and DoubleAccumulator.
 * In the JDK, this class provides striped cell-based CAS operations for reducing
 * contention across threads. In TeaVM's single-threaded JavaScript environment,
 * there is no contention, so all operations use a single value field.
 */
public class TStriped64 extends Number {
    protected long value;

    @Override
    public int intValue() {
        return (int) value;
    }

    @Override
    public long longValue() {
        return value;
    }

    @Override
    public float floatValue() {
        return value;
    }

    @Override
    public double doubleValue() {
        return value;
    }
}
